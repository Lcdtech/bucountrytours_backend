const Joi = require('joi');

const createBookingSchema = Joi.object({
  productId: Joi.string().uuid().optional().allow(null), 
  code: Joi.string().required(),
  productTitle: Joi.string().required(),
  productDate: Joi.string().required(),
  productTime: Joi.string().required(),
  total: Joi.string().required(),
  pickupPlace: Joi.string().required(),
  dropOffPlace: Joi.string().required(),
  currency: Joi.string().required(),
  firstName: Joi.string().required(),
  lastName: Joi.string().required(),
  paymentMethod: Joi.string().required(),
  bookingRef: Joi.string().allow(null),
  email: Joi.string().allow(null),
  phoneNo: Joi.string().allow(null),
  country: Joi.string().allow(null),
  organization: Joi.string().allow(null),
  noteForBooking: Joi.string().allow(null),
  noteForFinance: Joi.string().allow(null),
  noteForOperations: Joi.string().allow(null),
  emailTicketSend: Joi.boolean().optional(),
  markAsArrived: Joi.boolean().optional(),
  ticket: Joi.string().allow(null),
});
const updateBooking = Joi.object({
    productId: Joi.string().uuid().optional().allow(null), 
    code: Joi.string().required(),
    productTitle: Joi.string().required(),
    productDate: Joi.string().required(),
    productTime: Joi.string().required(),
    total: Joi.string().required(),
    pickupPlace: Joi.string().required(),
    dropOffPlace: Joi.string().required(),
    currency: Joi.string().required(),
    firstName: Joi.string().required(),
    lastName: Joi.string().required(),
    paymentMethod: Joi.string().required(),
    bookingRef: Joi.string().allow(null),
    email: Joi.string().allow(null),
    phoneNo: Joi.string().allow(null),
    country: Joi.string().allow(null),
    organization: Joi.string().allow(null),
    noteForBooking: Joi.string().allow(null),
    noteForFinance: Joi.string().allow(null),
    noteForOperations: Joi.string().allow(null),
    emailTicketSend: Joi.boolean().optional(),
    markAsArrived: Joi.boolean().optional(),
    ticket: Joi.string().allow(null),
});
module.exports = {
  createBookingSchema,
  updateBooking
};
